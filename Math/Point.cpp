#include "Point.h"


//-----------------------------------------------------------------------------
/**
  Конструктор Point
*/
//-----------------------------------------------------------------------------
Point::Point( double x, double y ):
  x( x ),
  y( y )
{
}


//-----------------------------------------------------------------------------
/**
  Конструктор копирования Point
*/
//-----------------------------------------------------------------------------
Point::Point( const Point& point )
{
  x = point.GetX();
  y = point.GetY();
}


//-----------------------------------------------------------------------------
/**
  возращаем координату x
*/
//-----------------------------------------------------------------------------
double Point::GetX() const
{
  return x;
}


//-----------------------------------------------------------------------------
/**
  возращаем координату y
*/
//-----------------------------------------------------------------------------
double Point::GetY() const
{
  return y;
}

